#ifndef First_H
#define First_H

/**
 * Uma classe simples que tem o operador sum
 */
class First {

    public:

        /**
         * O construtor da classe
         */
        First();

        /**
         * O método que retorna a soma de dois números
         * @param a o primeiro número inteiro que o método recebe
         * @param b o segundo número inteiro que o método recebe
         * @return o número inteiro resultante da soma dos dois valores passados como parâmetro
         */
        int sum (int a, int b);
};

#endif // First_H
